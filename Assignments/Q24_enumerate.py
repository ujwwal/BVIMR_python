# Q24. Demonstrate index and value enumeration
# This code uses enumerate to print index and value of elements in a list.

fruits = ['apple', 'banana', 'cherry']
for index, fruit in enumerate(fruits):
    print(f"Index: {index}, Fruit: {fruit}")

