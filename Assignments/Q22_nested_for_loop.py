# Q22. Demonstrate nested for loop statement
# This code prints a 3x3 multiplication table using nested for loops.

for i in range(1, 4):
    for j in range(1, 4):
        print(f"{i} x {j} = {i*j}", end="\t")
    print()  # Newline after each row
